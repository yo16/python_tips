# 配列から一部取り出し

list = ["A", "B", "C", "D", "E"]
print("対象リスト", list)

# [開始インデックス:終了インデックス]
print("[1:2]  ", list[1:2])
# [1:2]   ['B']

print("[1:-1] ", list[1:-1])
# [1:-1]  ['B', 'C', 'D']

print("[1:]   ", list[1:])
# [1:]    ['B', 'C', 'D', 'E']

print("[:2]   ", list[:2])
# [:2]    ['A', 'B']

print("[:]    ", list[:])
# [:]     ['A', 'B', 'C', 'D', 'E']

