# for文

# 回数指定
print('回数')
for i in range(0,4):	# ０～４未満、＋１ずつ
	print(i)
# 0,1,2,3
# ４回
for i in range(4):
	print(i)
# 0,1,2,3
# ４回



# list
print('list')
for i in [10,20,30]:
	print(i)
# 10,20,30

listTest = [40,50,60]
for i in listTest:
	print(i)
# 40,50,60





# tuple
print('tuple')
for i in (10,20,30):
	print(i)
# 10,20,30



